class RefundErrors{

    errorField;

    anyField;

    get error() {
        return this.errorField;
    }

    set error(value) {
        this.errorField = value;
    }

    get Any() {
        return this.anyField;
    }

    set Any(value) {
        this.anyField = value;
    }
}


 export default RefundErrors;