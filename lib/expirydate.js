class ExpiryDate{

    monthField;

    yearField;

    get month() {
        return this.monthField;
    }

    set month(value) {
        this.monthField = value;
    }

    get year() {
        return this.yearField;
    }

    set year(value) {
        this.yearField = value;
    }
}


 export default ExpiryDate;