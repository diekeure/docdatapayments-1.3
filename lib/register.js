class Register{

    idField;

    locationField;

    anyField;

    get id() {
        return this.idField;
    }

    set id(value) {
        this.idField = value;
    }

    get location() {
        return this.locationField;
    }

    set location(value) {
        this.locationField = value;
    }

    get Any() {
        return this.anyField;
    }

    set Any(value) {
        this.anyField = value;
    }
}


 export default Register;