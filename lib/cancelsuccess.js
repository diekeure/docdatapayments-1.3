class CancelSuccess{

    successField;

    resultField;

    anyField;

    get success() {
        return this.successField;
    }

    set success(value) {
        this.successField = value;
    }

    get result() {
        return this.resultField;
    }

    set result(value) {
        this.resultField = value;
    }

    get Any() {
        return this.anyField;
    }

    set Any(value) {
        this.anyField = value;
    }
}


 export default CancelSuccess;