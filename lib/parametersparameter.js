class ParametersParameter{

    nameField;

    valueField;

    get name() {
        return this.nameField;
    }

    set name(value) {
        this.nameField = value;
    }

    get Value() {
        return this.valueField;
    }

    set Value(value) {
        this.valueField = value;
    }
}


 export default ParametersParameter;