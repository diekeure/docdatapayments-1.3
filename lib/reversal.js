class Reversal{

    amountField;

    reasonField;

    anyField;

    get amount() {
        return this.amountField;
    }

    set amount(value) {
        this.amountField = value;
    }

    get reason() {
        return this.reasonField;
    }

    set reason(value) {
        this.reasonField = value;
    }

    get Any() {
        return this.anyField;
    }

    set Any(value) {
        this.anyField = value;
    }
}


 export default Reversal;