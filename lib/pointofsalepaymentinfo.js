class PointOfSalePaymentInfo{

    receiptField;

    anyField;

    get receipt() {
        return this.receiptField;
    }

    set receipt(value) {
        this.receiptField = value;
    }

    get Any() {
        return this.anyField;
    }

    set Any(value) {
        this.anyField = value;
    }
}


 export default PointOfSalePaymentInfo;