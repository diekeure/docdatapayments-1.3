class StatusSuccess{

    successField;

    reportField;

    anyField;

    get success() {
        return this.successField;
    }

    set success(value) {
        this.successField = value;
    }

    get report() {
        return this.reportField;
    }

    set report(value) {
        this.reportField = value;
    }

    get Any() {
        return this.anyField;
    }

    set Any(value) {
        this.anyField = value;
    }
}


 export default StatusSuccess;