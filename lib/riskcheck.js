class RiskCheck{

    checkField;

    anyField;

    scoreField;

    get check() {
        return this.checkField;
    }

    set check(value) {
        this.checkField = value;
    }

    get Any() {
        return this.anyField;
    }

    set Any(value) {
        this.anyField = value;
    }

    get score() {
        return this.scoreField;
    }

    set score(value) {
        this.scoreField = value;
    }
}


 export default RiskCheck;