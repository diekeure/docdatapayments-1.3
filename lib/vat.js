class Vat{

    amountField;

    rateField;

    get amount() {
        return this.amountField;
    }

    set amount(value) {
        this.amountField = value;
    }

    get rate() {
        return this.rateField;
    }

    set rate(value) {
        this.rateField = value;
    }
}


 export default Vat;