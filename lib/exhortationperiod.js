class ExhortationPeriod{

    numberOfDaysField;

    profileField;

    get numberOfDays() {
        return this.numberOfDaysField;
    }

    set numberOfDays(value) {
        this.numberOfDaysField = value;
    }

    get profile() {
        return this.profileField;
    }

    set profile(value) {
        this.profileField = value;
    }
}


 export default ExhortationPeriod;