class PointOfSalePaymentReceiptValuesValue{

    attributes = {
        'key': ''
    };
    $value;

    get key() {
        return this.attributes.key;
    }

    set key(value) {
        this.attributes.key = value;
    }

    get value() {
        return this.$value;
    }

    set value(value) {
        this.$value = value;
    }
}

export default PointOfSalePaymentReceiptValuesValue;